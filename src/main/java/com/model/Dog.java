package com.model;

public class Dog {

	private String color;
	private String name;
	private int age;
	private Food food;

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public Food getFood() {
		return food;
	}

	public void setFood(Food food) {
		this.food = food;
	}

	@Override
	public String toString() {
		return "Dog [color=" + color + ", name=" + name + ", age=" + age + ", food=" + food + "]";
	}

}
